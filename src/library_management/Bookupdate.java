    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        Connection conn=null;
        conn=DBconnection.connect();
        
        try
        {
            String btype="";     
            if (conn!=null) 
            {
               //JOptionPane.showMessageDialog(null,"Connected successfuly...");
               
               String sql="update books set booknum=?,bname=?,author=?,btype=?,publication=?,price=?,ISBN=?,edition=?,qty=? where bookid=?";
               PreparedStatement stmt=conn.prepareStatement(sql);
               
                      
               if(rdb_bref.isSelected())
               {
                   btype = rdb_bref.getText();
               }
               else
               {
                    btype = rdb_btext.getText();
               }
                        
               
               
               
               stmt.setString(1,txt_bnum.getText());
               stmt.setString(2,txt_bname.getText());
               stmt.setString(3,txt_author.getText());
               stmt.setString(4,btype);
               stmt.setString(5,txt_pub.getText());
               stmt.setString(6,txt_price.getText());
               stmt.setString(7,txt_isbn.getText());
               stmt.setString(8,txt_edition.getText());
               stmt.setString(9,txt_qty.getText());
               stmt.setString(10,txt_id.getText());
               
               
               
               int rowInserted=stmt.executeUpdate();
               if(rowInserted>0)
               {
                   JOptionPane.showMessageDialog(null,"A book is updated");
                   
               }
               else
               {
                   JOptionPane.showMessageDialog(null,"A book is not updated");
                }
                       
               
                
                  
            }
            else
             {
               JOptionPane.showMessageDialog(null,"Not Connected");
             }
        }catch (Exception e)
        {
        }

    }//GEN-LAST:event_button1ActionPerformed

