    private void button4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        Connection conn=DBconnection.connect();
        try{
            model.setRowCount(0);
            String sql="select * from books ";
            
            PreparedStatement stmt=conn.prepareStatement(sql);
            ResultSet rs=stmt.executeQuery();
            while(rs.next())
            {       
            String a=rs.getString("bookid");
            String b=rs.getString("booknum");
            String c=rs.getString("bname");
            String d=rs.getString("author");
            String e=rs.getString("btype");
            String f=rs.getString("publication");
            String g=rs.getString("price");
            String h=rs.getString("ISBN");
            String i=rs.getString("edition");
            String j=rs.getString("qty");
            model.addRow(new Object[]{a,b,c,d,e,f,g,h,i,j});
            
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_button4ActionPerformed

